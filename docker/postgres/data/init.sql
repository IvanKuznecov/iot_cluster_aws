CREATE TABLE mqtt_data (
    topic TEXT NOT NULL,
    payload JSONB NOT NULL,
    event_timestamp TIMESTAMPTZ NOT NULL,
    PRIMARY KEY (topic, event_timestamp)
) PARTITION BY RANGE (event_timestamp);


